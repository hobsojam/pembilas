# ── merge with existing annotations (existing entries win) ───────────────────
existing_path = root_dir / "data/annotations.json"
existing = json.loads(existing_path.read_text(encoding="utf-8"))

for word, affixes in annotations.items():
    if not affixes:          # skip placeholder entries (e.g. cerita defined twice)
        continue
    if word not in existing:
        existing[word] = affixes
    else:
        # fill in any affix not yet annotated
        for affix_id, entry in affixes.items():
            if affix_id not in existing[word]:
                existing[word][affix_id] = entry

existing_path.write_text(
    json.dumps(existing, ensure_ascii=False, indent=2),
    encoding="utf-8",
)
print(f"Done. {len(existing)} roots annotated.")
